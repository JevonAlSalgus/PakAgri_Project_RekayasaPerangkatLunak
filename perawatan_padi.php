                <ol>
                    <li><strong>1. Penyiraman</strong><br>Padi membutuhkan pasokan air yang cukup untuk tumbuh dengan
                        baik. Pastikan lahan padi selalu tergenang air setinggi sekitar 5-10 cm selama periode
                        pertumbuhan. Jika tidak ada pasokan air alami seperti hujan, Anda perlu menyediakan irigasi yang
                        cukup. Jaga agar air selalu tersedia, tetapi hindari genangan yang terlalu lama yang dapat
                        menyebabkan akar membusuk.</br></li>
                    <li><strong>2. Pemupukan</strong><br>Padi membutuhkan nutrisi yang cukup untuk pertumbuhannya.
                        Berikan pupuk yang sesuai secara teratur. Pupuk organik maupun pupuk kimia dapat digunakan.
                        Pupuk nitrogen, fosfor, dan kalium sangat penting untuk pertumbuhan tanaman padi. Ikuti petunjuk
                        dosis pemupukan yang dianjurkan untuk jenis pupuk yang Anda gunakan.</br></li>
                    <li><strong>3. Pengendalian Gulma</strong><br>Jaga kebersihan lahan padi dengan melakukan
                        pengendalian gulma secara teratur. Gulma dapat bersaing dengan tanaman padi untuk mendapatkan
                        nutrisi dan air. Lakukan penyiangan secara manual atau menggunakan alat pengendali gulma yang
                        sesuai untuk menghilangkan gulma yang tumbuh di antara tanaman padi.</br></li>
                    <li><strong>4. Pengendalian Hama dan Penyakit</strong><br>Pantau tanaman padi secara rutin untuk
                        mengidentifikasi adanya serangan hama atau penyakit. Jika Anda melihat tanda-tanda serangan
                        seperti daun yang berlubang, bercak pada daun, atau serangga yang merusak, segera ambil tindakan
                        pengendalian yang sesuai. Gunakan insektisida atau fungisida yang sesuai dan aman untuk tanaman
                        padi.</br></li>
                    <li><strong>5. Pemangkasan</strong><br>Tanaman padi tidak memerlukan pemangkasan rutin seperti
                        tanaman buah-buahan. Namun, jika ada daun yang rusak, kering, atau terinfeksi penyakit, Anda
                        dapat memangkasnya untuk menjaga kebersihan dan kesehatan tanaman.</br></li>
                    <li><strong>6. Panen</strong><br>Padi biasanya siap dipanen setelah sekitar 3-6 bulan tergantung
                        pada varietasnya. Periksa tanda-tanda kematangan padi seperti warna bulir, tekstur, dan
                        kekeringan tanaman. Panen dengan hati-hati menggunakan pisau atau alat panen yang tepat. Setelah
                        panen, keringkan padi dengan baik sebelum disimpan atau dijual.</br></li>
                </ol>
