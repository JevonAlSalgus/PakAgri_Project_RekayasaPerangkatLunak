                        serangan seperti daun yang berlubang atau berubah warna, segera ambil tindakan pengendalian yang
                        sesuai. Anda dapat menggunakan insektisida organik atau metode pengendalian hama alami seperti
                        menjebak serangga dengan perangkap atau menggunakan jaring penutup.</br></li>
                    <li><strong>5. Pemangkasan</strong><br>Tanaman wortel tidak memerlukan pemangkasan rutin seperti
                        tanaman buah-buahan, tetapi Anda dapat memangkas daun yang rusak atau layu untuk menjaga
                        kebersihan dan kesehatan tanaman.</br></li>
                    <li><strong>6. Penyimpanan Panen</strong><br>Setelah panen, pastikan untuk membersihkan wortel dari
                        tanah yang menempel. Potong daunnya sebelum menyimpan wortel di tempat yang sejuk dan kering.
