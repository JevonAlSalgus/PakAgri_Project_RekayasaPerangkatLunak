                        Pupuk dengan kandungan nitrogen, fosfor, dan kalium yang seimbang sangat dianjurkan. Ikuti
                        petunjuk dosis pemupukan yang tertera pada kemasan pupuk yang Anda gunakan.</br></li>
                    <li><strong>4. Perlindungan dari Hama dan Penyakit</strong><br>Pantau tanaman wortel secara rutin
