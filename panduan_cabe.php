                <p>Menanam cabai dapat menjadi kegiatan yang bermanfaat dan menyenangkan. Berikut panduannya</p>
                <ol>
                    <li><strong>1. Pilih varietas cabai yang sesuai</strong> Ada berbagai varietas cabai yang tersedia,
                        seperti cabai merah, cabai hijau, dan cabai rawit. Pilih varietas yang sesuai dengan preferensi
                        Anda dan kondisi tumbuh di daerah Anda.</li>
                    <li><strong>2. Persiapan bibit</strong>Jika Anda ingin menanam cabai dari biji, rendam biji dalam
                        air hangat selama beberapa jam sebelum menanam. Jika Anda membeli bibit cabai, pastikan bibit
                        tersebut sehat dan kuat.</li>
                    <li><strong>3. Persiapan lahan</strong> Pilih lahan yang terkena sinar matahari penuh dan memiliki
                        drainase yang baik. Bersihkan lahan dari gulma dan bebatuan. Jika tanah Anda kurang subur,
                        tambahkan pupuk organik atau pupuk kompos untuk meningkatkan kesuburan tanah.</li>
                    <li><strong>4. Penanaman bibit</strong>Tanam bibit cabai dengan jarak antar tanaman sekitar 30-40
                        cm. Buat lubang tanam dengan kedalaman sekitar 2-3 cm dan letakkan bibit cabai di setiap lubang.
                        Tutup dengan tanah dan padatkan sedikit.</li>
                    <li><strong>5. Penyiraman</strong> Cabai membutuhkan penyiraman yang cukup untuk menjaga kelembaban
                        tanah. Pastikan tanah tetap lembab, tetapi hindari penyiraman berlebihan yang dapat menyebabkan
                        pembusukan akar. Jaga agar tanah tetap lembab, terutama saat fase pertumbuhan awal.</li>
                    <li><strong>6. Pemupukan</strong> Berikan pupuk organik atau pupuk kandang pada saat penanaman untuk
                        memberikan nutrisi yang cukup kepada tanaman. Setelah itu, berikan pupuk nitrogen tambahan
                        setiap 3-4 minggu untuk mempromosikan pertumbuhan tanaman yang sehat.</li>
                    <li><strong>7. Penyiangan dan perawatan</strong> Lakukan penyiangan secara teratur untuk
                        menghilangkan gulma yang dapat bersaing dengan tanaman cabai. Juga, perhatikan tanda-tanda
                        serangan hama dan penyakit, seperti kutu daun atau penyakit layu. Jika diperlukan, lakukan
                        tindakan pengendalian yang sesuai.</li>
                    <li><strong>8. Pemanenan</strong> Cabai biasanya siap dipanen dalam waktu 60-90 hari setelah
                        penanaman, tergantung pada varietas yang Anda tanam. Perhatikan tanda-tanda kematangan seperti
                        ukuran cabai yang sudah cukup besar dan warna yang sesuai dengan varietasnya. Potong cabai
                        dengan hati-hati menggunakan gunting tajam.</li>
                </ol>

