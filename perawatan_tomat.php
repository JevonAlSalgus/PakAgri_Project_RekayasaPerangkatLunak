                <p>Menanam tomat memerlukan perhatian khusus. Berikut adalah langkah-langkah panduannya:</p>

                <ol>
                    <li><strong>1. Penyiraman</strong><br>Tanaman tomat membutuhkan penyiraman yang cukup untuk tumbuh
                        dengan baik. Siram tanaman secara teratur, terutama saat cuaca kering atau panas. Pastikan tanah
                        tetap lembab, tetapi hindari penyiraman berlebihan yang dapat menyebabkan akar membusuk.
                        Usahakan untuk menyiram tanaman di pagi hari atau sore hari, sehingga daun memiliki waktu untuk
                        mengering sebelum malam hari.</br></li>
                    <li><strong>2. Pemupukan</strong><br>Berikan pupuk yang sesuai untuk mendukung pertumbuhan tanaman
                        tomat. Pupuk organik atau pupuk kandang dapat digunakan untuk memberikan nutrisi yang cukup.
                        Pemupukan sekitar setiap 2 minggu sekali selama periode pertumbuhan aktif dapat membantu tanaman
                        tomat tumbuh dengan baik. Pilih pupuk yang kaya akan nitrogen, fosfor, dan kalium.</br></li>
                    <li><strong>3. Penyangga dan Pemangkasan</strong><br>Tanaman tomat cenderung tumbuh merambat, jadi
                        penting untuk memberikan penyangga atau kawat untuk menjaga tanaman tetap tegak. Selama
                        pertumbuhan, periksa tanaman secara teratur dan potong tunas samping yang tumbuh di ketiak daun
                        untuk mendorong pertumbuhan buah yang lebih baik.</br></li>
                    <li><strong>4. Pengendalian Gulma</strong><br>Jaga area tanaman tomat tetap bersih dari gulma dengan
                        melakukan penyiangan secara teratur. Gulma dapat bersaing dengan tanaman tomat untuk mendapatkan
                        nutrisi dan air. Lakukan penyiangan dengan hati-hati agar tidak merusak akar tomat yang masih
                        muda.</br></li>
                    <li><strong>5. Perlindungan dari Hama dan Penyakit</strong><br>Pantau tanaman tomat secara rutin
                        untuk mengidentifikasi adanya serangan hama atau penyakit. Jika Anda melihat tanda-tanda
                        serangan seperti daun yang berlubang, bercak pada daun, atau serangga yang merusak, segera ambil
                        tindakan pengendalian yang sesuai. Gunakan insektisida atau fungisida yang sesuai dan aman untuk
                        tanaman tomat.</br></li>
                    <li><strong>6. Pemanenan</strong><br>Tomat biasanya siap dipanen setelah buah mencapai ukuran dan
                        warna yang matang. Periksa buah secara rutin dan panen saat buah sudah matang. Potong tangkai
                        buah dengan hati-hati menggunakan pisau atau gunting tajam. Pastikan untuk menyimpan tomat yang
                        sudah dipanen di tempat yang sejuk dan kering.</br></li>
                </ol>

