            <ol>
                <li><strong>1. Persiapan lahan</strong> <br>Pilih lahan yang terkena sinar matahari penuh dan memiliki
                    drainase yang baik. Bersihkan lahan dari gulma dan bebatuan. Jika tanah Anda kurang subur, tambahkan
                    pupuk organik atau pupuk kompos untuk meningkatkan kesuburan tanah. </br></li>
                <li><strong>2. Penanaman benih</strong> <br>Tanam benih jagung pada saat suhu tanah mencapai sekitar
                    10-15 derajat Celsius. Buat lubang tanam dengan kedalaman sekitar 5-7 cm dan jarak antar lubang
                    sekitar 25-30 cm. Letakkan benih jagung di setiap lubang dan tutup dengan tanah. Pastikan untuk
                    memberikan jarak yang cukup antara barisan tanaman.</br></li>
                <li><strong>3. Penyiraman</strong> <br>Setelah menanam benih, berikan penyiraman yang cukup untuk
                    menjaga kelembaban tanah. Jagung membutuhkan tanah yang lembab, terutama pada fase awal pertumbuhan.
                    Namun, hindari penyiraman berlebihan yang dapat menyebabkan akar membusuk.</br></li>
                <li><strong>4. Pemupukan</strong> <br>Berikan pupuk nitrogen pada saat tanaman mencapai tinggi sekitar
                    15-20 cm. Pupuk nitrogen sangat penting untuk pertumbuhan daun yang baik. Gunakan pupuk sesuai
                    dengan petunjuk pada kemasan dan hindari pemupukan berlebihan yang dapat merusak tanaman.</br></li>
                <li><strong>5. Penyiangan dan perawatan</strong> <br>Lakukan penyiangan secara teratur untuk
                    menghilangkan gulma yang dapat bersaing dengan tanaman jagung. Perhatikan juga tanda-tanda serangan
                    hama dan penyakit, seperti ulat daun atau penyakit jamur. Jika diperlukan, lakukan tindakan
                    pengendalian yang sesuai.</br></li>
                <li><strong>6. Pemanenan</strong> <br>Jagung biasanya siap dipanen dalam waktu 70-90 hari setelah
                    penanaman, tergantung pada varietas yang Anda tanam. Perhatikan tanda-tanda kematangan seperti warna
                    tongkol yang berubah menjadi kuning dan biji jagung yang mengeras. Potong tongkol dengan pisau tajam
                    dan simpan dalam tempat yang kering.</br></li>

            </ol>
