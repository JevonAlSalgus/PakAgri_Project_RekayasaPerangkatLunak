            <div class="panduan-content">
                <p>Menanam wortel dapat menjadi kegiatan yang memuaskan. Berikut panduannya:</p>

                <ol>
                    <li><strong>1. Pemilihan Varietas</strong> <br>Ada banyak varietas wortel yang tersedia, jadi
                        pastikan Anda memilih yang sesuai dengan kondisi iklim dan kebutuhan Anda. Beberapa varietas
                        cocok untuk ditanam di daerah dengan iklim tropis, sedangkan yang lain lebih cocok untuk daerah
                        dengan iklim yang lebih dingin. Konsultasikan dengan petani lokal atau pakar pertanian untuk
                        mendapatkan rekomendasi yang tepat.</br></li>
                    <li><strong>2. Persiapan lahan</strong> <br>Persiapkan lahan dengan baik sebelum menanam wortel.
                        Pastikan tanahnya gembur, bebas dari batu dan gulma, dan memiliki drainase yang baik. Jika tanah
                        Anda berat atau berlempung, tambahkan bahan organik seperti kompos atau pupuk kandang untuk
                        meningkatkan drainase dan kesuburan tanah.</br></li>
                    <li><strong>3. Penyemaian benih</strong> <br>Benih wortel bisa ditanam langsung di lahan. Buat parit
                        dengan kedalaman sekitar 1-2 cm dan jarak antara parit sekitar 15-20 cm. Taburkan benih wortel
                        di dalam parit dengan jarak sekitar 5-7 cm antara benih. Tutup parit dengan tanah yang tipis dan
                        ringan, kemudian siram dengan air secukupnya.</br></li>
                    <li><strong>4. Perawatan tanaman</strong> <br>Pastikan tanaman wortel mendapatkan pasokan air yang
                        cukup. Selama periode pertumbuhan, tanaman wortel membutuhkan kelembaban yang konsisten. Jaga
                        juga agar tanaman terhindar dari gulma dengan melakukan penyiangan secara berkala. Anda juga
                        dapat memberikan pemupukan yang sesuai dengan dosis yang dianjurkan.</br></li>
                    <li><strong>7. Pemanenan:</strong> <br>Wortel biasanya siap dipanen setelah sekitar 2-3 bulan
                        tergantung pada varietasnya. Periksa ukuran wortel dan pastikan sudah mencapai ukuran yang
                        diinginkan sebelum dipanen. Gali wortel dengan hati-hati agar tidak merusak akar dan bersihkan
                        dari tanah yang menempel. Potong daunnya sebelum menyimpan wortel di tempat yang sejuk dan
                        kering.</br></li>

                </ol>

