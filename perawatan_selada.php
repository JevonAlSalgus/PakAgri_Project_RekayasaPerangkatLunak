                <p>Menanam selada memerlukan perhatian khusus. Berikut adalah langkah-langkah panduannya:</p>

                <ol>
                    <li><strong>1. Penyiraman</strong><br>Selada membutuhkan kelembaban yang konsisten. Pastikan tanah
                        tetap lembab, tetapi tidak terlalu basah. Siram tanaman secara teratur, terutama saat cuaca
                        kering atau panas. Hindari penyiraman berlebihan yang dapat menyebabkan pembusukan akar atau
                        penyakit jamur.</br></li>
                    <li><strong>2. Pencahayaan</strong><br>Selada tumbuh dengan baik di tempat yang mendapatkan sinar
                        matahari parsial atau naungan parsial. Jika Anda menanam selada di dalam ruangan, pastikan
                        tanaman mendapatkan cahaya yang cukup dari lampu tumbuh atau tempatkan di dekat jendela yang
                        mendapat sinar matahari yang cukup.</br></li>
                    <li><strong>3. Pemupukan</strong><br>Berikan pupuk yang sesuai untuk mendukung pertumbuhan selada.
                        Pupuk organik atau pupuk kandang dapat digunakan untuk memberikan nutrisi yang cukup. Pemupukan
                        sekitar satu bulan sekali dapat membantu tanaman selada tumbuh dengan baik. Ikuti petunjuk dosis
                        pemupukan yang tertera pada kemasan pupuk yang Anda gunakan.</br></li>
                    <li><strong>4. Pengendalian Gulma</strong><br>Jaga kebersihan area tanaman selada dengan melakukan
                        penyiangan secara teratur. Bersihkan gulma yang tumbuh di sekitar tanaman untuk menghindari
                        persaingan nutrisi dan air. Anda juga dapat menggunakan mulsa organik seperti jerami atau serbuk
                        gergaji untuk menekan pertumbuhan gulma.</br></li>
                    <li><strong>5. Perlindungan dari Hama dan Penyakit</strong><br>Pantau tanaman selada secara rutin
                        untuk mengidentifikasi adanya serangan hama atau penyakit. Jika Anda melihat tanda-tanda
                        serangan seperti daun yang berlubang atau berubah warna, segera ambil tindakan pengendalian yang
                        sesuai. Gunakan insektisida organik atau metode pengendalian hama alami seperti menjebak
                        serangga dengan perangkap atau menggunakan jaring penutup.</br></li>
                    <li><strong>6. Pemanenan</strong><br>Selada biasanya siap dipanen setelah sekitar 4-8 minggu
                        tergantung pada varietasnya. Periksa ukuran dan tekstur daun untuk menentukan kapan selada siap
                        dipanen. Potong daun yang ingin Anda panen dengan pisau atau gunting tajam. Biarkan daun yang
                        tersisa tetap tumbuh untuk panen berikutnya.</br></li>
                </ol>

