                <p>Menanam semangka memerlukan perhatian khusus. Berikut adalah langkah-langkah panduannya:</p>

                <ol>
                    <li><strong>1. Penyiraman</strong><br>Semangka membutuhkan air yang cukup untuk tumbuh dengan baik.
                        Siram tanaman secara teratur, terutama saat cuaca kering atau panas. Pastikan tanah tetap
                        lembab, tetapi hindari penyiraman berlebihan yang dapat menyebabkan akar membusuk. Selama
                        periode pertumbuhan aktif, semangka membutuhkan lebih banyak air, sedangkan saat buah sudah
                        matang, jumlah penyiraman dapat dikurangi.</br></li>
                    <li><strong>2. Pemupukan</strong><br>Berikan pupuk yang sesuai untuk mendukung pertumbuhan semangka.
                        Pupuk organik atau pupuk kandang dapat digunakan untuk memberikan nutrisi yang cukup. Pemupukan
                        sekitar setiap 2-3 minggu sekali selama periode pertumbuhan aktif dapat membantu tanaman
                        semangka tumbuh dengan baik. Pilih pupuk yang kaya akan nitrogen, fosfor, dan kalium.</br></li>
                    <li><strong>3. Pengendalian Gulma</strong><br>Jaga area tanaman semangka tetap bersih dari gulma
                        dengan melakukan penyiangan secara teratur. Gulma dapat bersaing dengan tanaman semangka untuk
                        mendapatkan nutrisi dan air. Lakukan penyiangan dengan hati-hati agar tidak merusak akar
                        semangka yang masih muda.</br></li>
                    <li><strong>4. Penyulaman dan Penyangga</strong><br>Jika tanaman semangka tumbuh merambat, Anda
                        dapat menggunakan penyangga atau pagar untuk mendukung tanaman. Pastikan untuk melakukan
                        penyulaman dengan hati-hati agar tidak merusak akar atau batang tanaman.</br></li>
                    <li><strong>5. Perlindungan dari Hama dan Penyakit</strong><br>Pantau tanaman semangka secara rutin
                        untuk mengidentifikasi adanya serangan hama atau penyakit. Jika Anda melihat tanda-tanda
                        serangan seperti daun yang berlubang, bercak pada daun, atau serangga yang merusak, segera ambil
                        tindakan pengendalian yang sesuai. Gunakan insektisida atau fungisida yang sesuai dan aman untuk
                        tanaman semangka.</br></li>
                    <li><strong>6. Pemanenan</strong><br>Semangka biasanya siap dipanen setelah sekitar 2-3 bulan
                        tergantung pada varietasnya. Periksa tanda-tanda kematangan semangka seperti warna kulit yang
                        berubah, suara "dengkuran" ketika diketuk, dan ukuran yang sesuai. Potong semangka dengan
                        hati-hati menggunakan pisau atau gunting tajam. Pastikan untuk menyimpan semangka yang sudah
                        dipanen di tempat yang sejuk dan kering.</br></li>
                </ol>

