                        menguning dan layu. Gali kentang dengan hati-hati menggunakan garpu atau sekop untuk menghindari
                        merusak umbi. Biarkan kentang kering di tempat yang teduh selama beberapa jam sebelum
                        menyimpannya.</br></li>
