                Panduan Menanam Kentang
            </div>
            <p>Pertahankan ketinggian air yang sesuai dan tanam kentang dalam barisan untuk memaksimalkan hasil panen
                yang berkualitas.</p>

            <ol>
                <li><strong>1. Pilih varietas kentang yang sesuai</strong> <br>Ada berbagai varietas kentang yang
                    tersedia, seperti kentang merah, kentang kuning, dan kentang ungu. Pilih varietas yang sesuai dengan
                    preferensi Anda dan kondisi tumbuh di daerah Anda. </br></li>
                <li><strong>2. Persiapan bibit</strong> <br>Potong kentang menjadi potongan kecil dengan setidaknya dua
                    mata tunas pada setiap potongan. Biarkan potongan kentang kering selama beberapa hari untuk
                    membentuk lapisan kulit yang melindungi dari penyakit.</br></li>
                <li><strong>3. Persiapan lahan</strong> <br>Pilih lahan yang terkena sinar matahari penuh dan memiliki
                    drainase yang baik. Bersihkan lahan dari gulma dan bebatuan. Jika tanah Anda kurang subur, tambahkan
                    pupuk organik atau pupuk kompos untuk meningkatkan kesuburan tanah.</br></li>
                <li><strong>4. Penanaman bibit</strong> <br>Tanam potongan kentang dengan mata tunas menghadap ke atas.
                    Buat lubang tanam dengan kedalaman sekitar 10-15 cm dan jarak antar lubang sekitar 30-40 cm.
                    Letakkan potongan kentang di setiap lubang dan tutup dengan tanah. Pastikan untuk memberikan jarak
                    yang cukup antara barisan tanaman.</br></li>
                <li><strong>5. Penyiraman</strong> <br>Kentang membutuhkan penyiraman yang cukup untuk menjaga
                    kelembaban tanah. Namun, hindari penyiraman berlebihan yang dapat menyebabkan busuk akar. Pastikan
                    tanah tetap lembab tanpa terlalu basah.</br></li>
                <li><strong>6. Pemupukan</strong> <br>Berikan pupuk organik atau pupuk kandang pada saat penanaman untuk
                    memberikan nutrisi yang cukup kepada tanaman. Setelah itu, berikan pupuk nitrogen tambahan setelah
                    tanaman tumbuh sekitar 3-4 minggu. Ikuti petunjuk pada kemasan pupuk untuk dosis yang tepat.</br>
                </li>
                <li><strong>7. Penyiangan dan perawatan</strong> <br> Lakukan penyiangan secara teratur untuk
                    menghilangkan gulma yang dapat bersaing dengan tanaman kentang. Juga, perhatikan tanda-tanda
                    serangan hama dan penyakit, seperti kutu daun atau penyakit jamur. Jika diperlukan, lakukan tindakan
                    pengendalian yang sesuai.</br></li>
                <li><strong>8. Pemanenan</strong> <br>Kentang biasanya siap dipanen dalam waktu 70-120 hari setelah
                    penanaman, tergantung pada varietas yang Anda tanam. Perhatikan tanda-tanda kematangan seperti daun
                    yang mulai menguning dan mati. Gali kentang dengan hati-hati menggunakan sekop atau garpu
                    taman.</br></li>
            </ol>

