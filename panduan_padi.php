                <ol>
                    <li><strong>1. Pemilihan Varietas</strong> <br>Ada banyak varietas padi yang tersedia, jadi pastikan
                        Anda memilih yang sesuai dengan kondisi iklim, jenis tanah, dan kebutuhan Anda. Konsultasikan
                        dengan petani lokal atau pakar pertanian untuk mendapatkan rekomendasi yang tepat.</br></li>
                    <li><strong>2. Persiapan lahan</strong> <br>Persiapkan lahan dengan baik sebelum menanam padi.
                        Pastikan tanahnya subur, bebas dari gulma, dan memiliki drainase yang baik. Anda juga perlu
                        memastikan bahwa lahan memiliki akses ke pasokan air yang cukup.</br></li>
                    <li><strong>3. Penyemaian benih</strong> <br>Benih padi dapat ditanam terlebih dahulu dalam wadah
                        atau langsung di lahan. Jika menggunakan wadah, benih dapat disemai dalam pot atau polibag. Jika
                        menanam langsung di lahan, buat parit atau bedengan dengan jarak yang sesuai antara
                        tanaman.</br></li>
                    <li><strong>4. Perawatan tanaman</strong> <br>Pastikan tanaman padi mendapatkan pasokan air yang
                        cukup. Jika tidak ada pasokan air alami seperti hujan, Anda perlu menyediakan irigasi yang
                        cukup. Jaga juga agar tanaman terhindar dari hama dan penyakit dengan melakukan pemantauan rutin
                        dan pengendalian yang tepat.
                        </br> </li>
                    <li><strong>5. Pemupukan</strong> <br> Berikan pupuk yang sesuai untuk mendukung pertumbuhan tanaman
                        padi. Pupuk organik maupun pupuk kimia dapat digunakan, namun pastikan untuk mengikuti dosis
                        yang dianjurkan agar tidak merusak tanaman.</br></li>
                    <li><strong>6. Panen</strong> <br>Padi biasanya siap dipanen setelah sekitar 3-6 bulan tergantung
                        pada varietasnya. Periksa tanda-tanda kematangan padi seperti warna bulir, tekstur, dan
                        kekeringan tanaman. Panen dengan hati-hati dan pastikan untuk mengeringkan padi dengan baik
                        sebelum disimpan atau dijual.</br></li>
                </ol>

